    int problem_tapma(String str){
        if(str.length()>1){
            JOptionPane.showMessageDialog(this,"Herf düzgün daxil edilmeyib.\n Haqqinizdan cixilmayacaq.");
            return 0;
        }
        if(str.length()==0){
        JOptionPane.showMessageDialog(this,"Herf daxil edilmeyib.\n Haqqinizdan cixilmayacaq.");
            return 0;    
        }
        return 1;
    }
    
