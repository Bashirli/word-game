    private void herf_daxilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_herf_daxilActionPerformed
        // TODO add your handling code here:

        if(herfhaqqi==0){
            JOptionPane.showMessageDialog(this, "Herf haqqiniz qalmadi.");
            jTextField1.setText("");
            return;
        }
        if(problem_tapma(jTextField1.getText().toString())==0){
            return;
        }
        for(int i=0;i<secilen_soz.length();i++){
            if(secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){

                if(i==0&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    bir.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==1&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    iki.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==2&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    uc.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==3&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    dord.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==4&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){

                    bes.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==5&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    alti.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
                if(i==6&&secilen_soz.charAt(i)==jTextField1.getText().toUpperCase().charAt(0)){
                    yeddi.setText(" "+jTextField1.getText().toString().toUpperCase());

                }
            }

        }
          String bild=sayac+". "+jTextField1.getText().toString();
        model.addElement(bild);
      sayac++;
        jTextField1.setText("");
        switch (secilen_soz.length()) {
            case 4:
            bitime_soz=bir.getText().toString().trim()+iki.getText().toString().trim()+uc.getText().toString().trim()+dord.getText().toString().trim();
            break;
            case 5:
            bitime_soz=bir.getText().toString().trim()+iki.getText().toString().trim()+uc.getText().toString().trim()+dord.getText().toString().trim()+bes.getText().toString().trim();
            break;
            case 6:
            bitime_soz=bir.getText().toString().trim()+iki.getText().toString().trim()+uc.getText().toString().trim()+dord.getText().toString().trim()+bes.getText().toString().trim()+alti.getText().toString().trim();
            break;
            case 7:
            bitime_soz=bir.getText().toString().trim()+iki.getText().toString().trim()+uc.getText().toString().trim()+dord.getText().toString().trim()+bes.getText().toString().trim()+alti.getText().toString().trim()+yeddi.getText().toString().trim();
            break;
        }

        if(bitime_soz.equals(secilen_soz)){
            JOptionPane.showMessageDialog(this, "Oyun bitdi. Tebrikler "+oyuncu+"! \n Oyunu qazandınız.");
            setVisible(false);
            //EKRAN
            GameOver go=new GameOver();
go.oyuncu=oyuncu;
go.setVisible(true);
            setVisible(false);
            return;
        }
     
        herfhaqqi--;
        herf_haqq.setText("Herf haqq: "+herfhaqqi);
          if(sozhaqqi==0&&herfhaqqi==0){
            JOptionPane.showMessageDialog(this, "Oyunu uduzdunuz. \n "+secilen_soz+" bu sözü tapa bilmediniz.");
            setVisible(false);
            //EKRAN
            GameOver go=new GameOver();
            go.oyuncu=oyuncu;
            go.setVisible(true);
            setVisible(false);
            return;
        }
    }//GEN-LAST:event_herf_daxilActionPerformed

